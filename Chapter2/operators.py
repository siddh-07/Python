# Comparison Operators Examples

boolean1 = 10
boolean2 = 20

print("\nComparison Operators:")
print("10 Equal to 20:", boolean1 == boolean2)               
print("10 Not Equal 20:", boolean1 != boolean2)
print("10 Greater Than 20:", boolean1 > boolean2)
print("10 Less Than 20:", boolean1 < boolean2)
print("10 Greater Than or Equal To 20:", boolean1 >= boolean2)
print("10 Less Than or Equal To 20:", boolean1 <= boolean2)

# Logical Operators Examples    
# Truth table for AND
print("\nLogical Operators:")
print("Truth Table for AND:")
print("True AND True:", True and True)
print("True AND False:", True and False)
print("False AND True:", False and True)
print("False AND False:", False and False)
# Truth table for OR
print("\nTruth Table for OR:")
print("True OR True:", True or True)
print("True OR False:", True or False)
print("False OR True:", False or True)
print("False OR False:", False or False)
# Truth table for NOT
print("\nTruth Table for NOT:")
print("NOT True:", not True) # True becomes False
print("NOT False:", not False) # False becomes True


