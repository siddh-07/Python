# This script demonstrates different data types in Python.

a = 1
b = 2.5
c = "Hello, World!"

print("Data Types Examples:")
print("Type of a (1):", type(a))          
print("Type of b (2.5):", type(b))
print("Type of c ('Hello, World!'):", type(c))

a = "32.55"
b = float(a)
print("\nType Conversion Examples:")
print("Original a (string):", a, "Type:", type(a))
print("Converted b (float):", b, "Type:", type(b))


