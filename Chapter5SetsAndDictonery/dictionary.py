 # Example of using dictionaries in Python
 # Value at left side is the key and value at right side is the value associated with that key
 # Its is mutable i.e we can change, add or remove key-value pairs after its creation
 # Dictionaries are unordered collections of key-value pairs
 # In the dictionary, keys must be unique and immutable (like strings, numbers, or tuples), while values can be of any data type and can be duplicated.
 
marks = {
    "Alice": 85,
    "Bob": 92,
    "Charlie": 78,
    "Siddh" : 99,
    }

print("Marks Dictionary:", marks)